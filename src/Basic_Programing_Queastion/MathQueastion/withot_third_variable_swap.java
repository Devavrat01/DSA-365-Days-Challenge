public class withot_third_variable_swap {
    public static void main(String[] args) {
        int a=5;
        int b=6;
        System.out.println("a is value:"+a);
        System.out.println("b  is value: "+b);
        a=a+b;
        b=a-b;
        a=a-b;
        System.out.println("a is value:"+a);
        System.out.println("b  is value: "+b);

    }
}
